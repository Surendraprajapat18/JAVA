public class Biography {
    public static void main(String[] args) {
        

        String name = "Surendra"; // name (text)
        int age = 20; // age (whole number)
        String country = "India"; // country (text)
        String sport = "Cricket"; // sport (text)
        int  hours = 2; // hours (whole number)
        String game = "Rubik's Cube"; // game (text)
        String subject = "Maths"; // subject (text)
        char grade = 'A'; // grade (character)
     
        System.out.println("My name is " + name + ". I'm " + age + " years old, and I'm from " + country);
        System.out.println("My favourite sport is " + sport + ". I play for " + hours +  " hours a week");
        System.out.println("When I'm tired, I like to play " + game + ".");
        System.out.println("In school, my favourite subject was " + subject + ", I scored a " + grade + ".");

    }
}
